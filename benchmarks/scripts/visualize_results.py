import pandas as pd
from pathlib import Path
from tabulate import tabulate

# 1. 경로 설정
BASE_DIR = Path(__file__).resolve().parent.parent
INPUT_FILE = BASE_DIR / "results" / "mini_benchmark_results.csv"
OUTPUT_DIR = BASE_DIR / "results"
OUTPUT_FILE = OUTPUT_DIR / "mini_summary_table.md"

def generate_table():
    if not INPUT_FILE.exists():
        print(f"[!] {INPUT_FILE} 파일이 없습니다. 먼저 실험을 완료하세요.")
        return

    # 데이터 로드
    df = pd.read_csv(INPUT_FILE)

    # 2. 데이터 가독성을 위한 정리
    # 숫자가 없는 칸(N/A)은 '-'로 표시
    df = df.fillna('-')

    # 3. 터미널용 표 생성 (Tabulate 사용)
    # 이미지에서 보신 것처럼 깔끔한 격자(grid) 스타일로 출력합니다.
    table_view = tabulate(df, headers='keys', tablefmt='grid', showindex=False)
    
    # 4. Markdown용 표 생성
    # README 등에 바로 붙여넣을 수 있는 형식입니다.
    markdown_view = tabulate(df, headers='keys', tablefmt='github', showindex=False)

    # 결과 출력
    print("\n" + "="*20 + " [ AutoML Benchmark Summary ] " + "="*20)
    print(table_view)
    print("="*65 + "\n")

    # 5. 파일로 저장 (Markdown 형식)
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write("# 📊 AutoML Benchmark Performance Table\n\n")
        f.write(f"**Last Updated:** {pd.Timestamp.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n")
        f.write(markdown_view)
        f.write("\n\n---\n*Generated by visualize_results.py*")

    print(f"[!] 표 형태 결과가 저장되었습니다: {OUTPUT_FILE}")

if __name__ == "__main__":
    generate_table()